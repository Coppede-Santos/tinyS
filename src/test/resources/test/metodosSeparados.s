class Rectangle{
    pub Double height; // Alto del rectangulo
    pub Double width;  // Ancho del rectangulo
}











impl Rectangle {

    fn Double perimeter ( Double h, Double w ) {
        ret h*2 + w*2;
    }
    
    fn Double area ( Double h, Double w ) {
        ret h*w;
    }

    .(Double h, Double w) {
        height=h;
        width=w;
    }

}